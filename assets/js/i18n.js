  function formatNumber(value, options = {}) {
    return new Intl.NumberFormat(getLocale(), options).format(value);
  }

  function formatCurrency(cents) {
    return new Intl.NumberFormat(getLocale(), { style: 'currency', currency: 'BRL' }).format(cents / 100);
  }

  function formatDate(isoDate) {
    if (!isoDate) return t('common.none');
    const [year, month, day] = String(isoDate).split('-').map(Number);
    if (!Number.isInteger(year) || !Number.isInteger(month) || !Number.isInteger(day)) return t('common.none');
    return new Intl.DateTimeFormat(getLocale()).format(new Date(year, month - 1, day, 12));
  }

  function formatMonth(month) {
    const [year, monthNumber] = String(month).split('-').map(Number);
    if (!Number.isInteger(year) || !Number.isInteger(monthNumber)) return month;
    return new Intl.DateTimeFormat(getLocale(), { month: 'long', year: 'numeric' }).format(new Date(year, monthNumber - 1, 1, 12));
  }

  function formatPercent(value, options = {}) {
    const digits = { minimumFractionDigits: 2, maximumFractionDigits: 4, ...options };
    return `${formatNumber(value * 100, digits)}%`;
  }

  function formatRatePercent(ratePercent, options = {}) {
    const digits = { minimumFractionDigits: 2, maximumFractionDigits: 4, ...options };
    return formatNumber(ratePercent, digits);
  }

  function parseLocalizedNumber(rawValue) {
    let value = String(rawValue ?? '').trim();
    if (!value) return Number.NaN;
    value = value
      .replace(/[^\d,.\-]/g, '')
      .replace(/(?!^)-/g, '');
    if (!value || value === '-') return Number.NaN;

    const lastComma = value.lastIndexOf(',');
    const lastDot = value.lastIndexOf('.');
    let decimalSeparator = lastComma > lastDot ? ',' : lastDot > lastComma ? '.' : null;

    if (decimalSeparator && (lastComma === -1 || lastDot === -1)) {
      const parts = value.split(decimalSeparator);
      const hasMultipleSeparators = parts.length > 2;
      const integerPart = parts[0].replace('-', '');
      const lastPart = parts.at(-1);
      const looksLikeThousands = (
        lastPart.length === 3
        && integerPart !== '0'
        && parts.slice(1).every((part) => part.length === 3)
      );
      if (hasMultipleSeparators && looksLikeThousands) decimalSeparator = null;
      if (!hasMultipleSeparators && looksLikeThousands) decimalSeparator = null;
    }

    if (decimalSeparator) {
      const integerPart = value.slice(0, value.lastIndexOf(decimalSeparator)).replace(/[,.]/g, '');
      const decimalPart = value.slice(value.lastIndexOf(decimalSeparator) + 1).replace(/[,.]/g, '');
      value = `${integerPart || '0'}.${decimalPart}`;
    }

    if (!decimalSeparator) value = value.replace(/[,.]/g, '');
    if (!/^-?\d+(\.\d+)?$/.test(value)) return Number.NaN;
    return Number(value);
  }

  function setLanguage(language) {
    currentLanguage = normalizeLanguage(language);
    try {
      globalScope.localStorage?.setItem(LANGUAGE_STORAGE_KEY, currentLanguage);
    } catch (error) {
      // Language selection remains available even when storage is blocked.
    }

    if (globalScope.document) {
      globalScope.document.documentElement.lang = languageConfig[currentLanguage].htmlLang;
      globalScope.document.title = t('metadata.title');
      const metaDescription = globalScope.document.querySelector('meta[name="description"]');
      if (metaDescription) metaDescription.setAttribute('content', t('metadata.description'));
      const socialMetadata = [
        ['meta[property="og:title"]', 'metadata.title'],
        ['meta[property="og:description"]', 'metadata.description'],
        ['meta[property="og:site_name"]', 'header.title'],
        ['meta[name="twitter:title"]', 'metadata.title'],
        ['meta[name="twitter:description"]', 'metadata.description'],
      ];
      socialMetadata.forEach(([selector, key]) => {
        globalScope.document.querySelector(selector)?.setAttribute('content', t(key));
      });
      globalScope.document.querySelector('meta[property="og:locale"]')?.setAttribute(
        'content',
        languageConfig[currentLanguage].locale.replace('-', '_'),
      );
    }
    return currentLanguage;
  }

